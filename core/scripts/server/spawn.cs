function pickPlayerSpawnPoint(%buildGroup)
{
  if (%buildGroup)
    %playerSpawnPoint = %buildGroup.getShipObject().getSpawnPoint();

  if (!isObject(%playerSpawnPoint))
    return "0 0 10";
  return %playerSpawnPoint;
}






